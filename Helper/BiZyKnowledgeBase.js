// Helper function to search knowledge base
export const searchKnowledgeBase = (query) => {
  const searchTerm = query.toLowerCase();
  const results = [];

  // Search through all sections
  Object.keys(BiZZyKnowledgeBase).forEach(section => {
    const sectionData = BiZZyKnowledgeBase[section];
    
    if (typeof sectionData === 'string' && sectionData.toLowerCase().includes(searchTerm)) {
      results.push({ section, content: sectionData });
    } else if (Array.isArray(sectionData)) {
      sectionData.forEach((item, index) => {
        if (typeof item === 'string' && item.toLowerCase().includes(searchTerm)) {
          results.push({ section, content: item });
        } else if (typeof item === 'object') {
          Object.keys(item).forEach(key => {
            if (typeof item[key] === 'string' && item[key].toLowerCase().includes(searchTerm)) {
              results.push({ section, content: item[key] });
            }
          });
        }
      });
    } else if (typeof sectionData === 'object') {
      Object.keys(sectionData).forEach(key => {
        if (typeof sectionData[key] === 'string' && sectionData[key].toLowerCase().includes(searchTerm)) {
          results.push({ section, content: sectionData[key] });
        }
      });
    }
  });

  return results;
};

